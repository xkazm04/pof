/**
 * Generate TOOLS-REFERENCE.md from the live tool registry + recorded example responses.
 * Tests-as-docs: each documented tool is invoked through the real MCP server against a
 * running backend, and its (redacted) response is embedded. Run: npm run gen:docs
 * (requires the PoF backend up; POF_APP_ORIGIN to point at it).
 */
import { writeFileSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, join } from 'node:path';
import { connectMcp, backendReachable, ORIGIN } from '../dist/harness.js';
import { EXAMPLE_SKIP } from '../dist/coverage.js';
import { PIPELINE_TOOLS } from '../dist/tools/pipeline.js';
import { HARNESS_TOOLS } from '../dist/tools/harness.js';
import { SIM_TOOLS } from '../dist/tools/sims.js';
import { UE_TOOLS } from '../dist/tools/ue.js';
import { DESIGN_TOOLS } from '../dist/tools/design.js';

const here = dirname(fileURLToPath(import.meta.url));
const OUT = join(here, '..', 'TOOLS-REFERENCE.md');

const FAMILIES = [
  ['Pipeline loop', PIPELINE_TOOLS],
  ['Harness loop', HARNESS_TOOLS],
  ['Simulation & balance', SIM_TOOLS],
  ['UE truth & growth', UE_TOOLS],
  ['Design truth & quality signals', DESIGN_TOOLS],
];

const VOLATILE = /(updatedAt|createdAt|generatedAt|completedAt|lastRunAt|timestamp|durationMs|_ms|checksum|sha256|sha)/i;

function redact(v, depth = 0) {
  if (Array.isArray(v)) return v.slice(0, 2).map((x) => redact(x, depth + 1)).concat(v.length > 2 ? [`…(+${v.length - 2} more)`] : []);
  if (v && typeof v === 'object') {
    if (depth > 3) return '…';
    const out = {};
    for (const [k, val] of Object.entries(v)) out[k] = VOLATILE.test(k) ? '<redacted>' : redact(val, depth + 1);
    return out;
  }
  if (typeof v === 'string' && v.length > 200) return v.slice(0, 200) + '…';
  return v;
}

function schemaLines(schema) {
  const props = schema.properties ?? {};
  const req = new Set(schema.required ?? []);
  const keys = Object.keys(props);
  if (!keys.length) return '_(no arguments)_';
  return keys.map((k) => `- \`${k}\`${req.has(k) ? ' **(required)**' : ''}: ${props[k].type ?? 'any'}${props[k].description ? ` — ${props[k].description}` : ''}`).join('\n');
}

async function main() {
  if (!(await backendReachable())) {
    console.error(`Backend not reachable at ${ORIGIN}; start the PoF dev server first.`);
    process.exit(1);
  }
  const mcp = await connectMcp();
  const parts = [];
  parts.push('# pof-mcp — Tools Reference', '');
  parts.push('> Generated by `npm run gen:docs` from the live registry + recorded example responses.', '> Responses are redacted (volatile ids/timestamps) and truncated. Do not hand-edit.', '');
  const total = FAMILIES.reduce((n, [, t]) => n + t.length, 0);
  parts.push(`**${total} tools** across ${FAMILIES.length} families. Origin: \`${ORIGIN}\`.`, '');

  for (const [family, tools] of FAMILIES) {
    parts.push(`## ${family}`, '');
    for (const tool of tools) {
      parts.push(`### \`${tool.name}\``, '', tool.description, '', '**Arguments:**', schemaLines(tool.inputSchema), '');
      if (tool.example) {
        const res = await mcp.call(tool.name, tool.example.args);
        parts.push('**Example call:**', '```json', JSON.stringify(tool.example.args, null, 2), '```');
        if (tool.example.note) parts.push(`> ${tool.example.note}`, '');
        const body = res.isError ? `ERROR: ${res.text}` : JSON.stringify(redact(res.json), null, 2);
        const capped = body.split('\n').slice(0, 40).join('\n');
        parts.push('**Example response:**', '```json', capped + (body.split('\n').length > 40 ? '\n…(truncated)' : ''), '```', '');
      } else {
        parts.push(`> No static example — ${EXAMPLE_SKIP[tool.name] ?? 'recorded dynamically by the quality/growth suites.'}`, '');
      }
    }
  }

  await mcp.close();
  writeFileSync(OUT, parts.join('\n'));
  console.log(`Wrote ${OUT} (${total} tools).`);
}

main().catch((e) => {
  console.error(e);
  process.exit(1);
});
