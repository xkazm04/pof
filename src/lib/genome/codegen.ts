export function generateAttributeInitTable(g: CharacterGenome): string {
  const safeName = sanitizeCppIdentifier(g.name);
  const rows = [
    `${csvEscape(`${safeName}.HP`)},${g.attributes.baseHP},${g.attributes.hpPerLevel}`,
    `${csvEscape(`${safeName}.Stamina`)},${g.attributes.baseStamina},${g.attributes.staminaPerLevel}`,
    `${csvEscape(`${safeName}.Mana`)},${g.attributes.baseMana},${g.attributes.manaPerLevel}`,
    `${csvEscape(`${safeName}.Armor`)},${g.attributes.baseArmor},${g.attributes.armorPerLevel}`,
    `${csvEscape(`${safeName}.StaminaRegen`)},${g.attributes.staminaRegenPerSec},0`,
    `${csvEscape(`${safeName}.ManaRegen`)},${g.attributes.manaRegenPerSec},0`,
  ];
  return `RowName,BaseValue,ScalingPerLevel\n${rows.join('\n')}`;
}
