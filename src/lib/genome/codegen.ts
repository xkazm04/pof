import type { CharacterGenome } from '@/types/character-genome';

/** Strip to valid C++ identifier chars [A-Za-z0-9_], ensure non-empty */
export function sanitizeCppIdentifier(raw: string): string {
  const cleaned = raw.replace(/[^A-Za-z0-9_]/g, '');
  return cleaned || 'Unnamed';
}

/** Strip newlines and control chars from text embedded in C++ // comments */
export function sanitizeCppComment(raw: string): string {
  return raw.replace(/[\r\n\\\x00-\x1f]/g, ' ').trim();
}

/** Escape a value for CSV: wrap in quotes if it contains comma, quote, or newline */
export function csvEscape(value: string): string {
  if (/[,"\r\n]/.test(value)) {
    return `"${value.replace(/"/g, '""')}"`;
  }
  return value;
}

export function generateSubclassHeader(g: CharacterGenome): string {
  const className = `AARPG${sanitizeCppIdentifier(g.name)}Character`;
  return `// Auto-generated by PoF Character Genome System
// Genome: ${sanitizeCppComment(g.name)} v${g.version} — ${sanitizeCppComment(g.description)}
// Author: ${sanitizeCppComment(g.author)} | ${g.updatedAt}

#pragma once

#include "CoreMinimal.h"
#include "Character/ARPGCharacterBase.h"
#include "${className}.generated.h"

UCLASS()
class POF_API ${className} : public AARPGCharacterBase
{
\tGENERATED_BODY()

public:
\t${className}();

protected:
\tvirtual void BeginPlay() override;
\tvirtual void InitializeAttributes() override;
};`;
}

